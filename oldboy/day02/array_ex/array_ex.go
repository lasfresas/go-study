package main

import "fmt"

// array数组练习题
// 找出数组中的和为指定值的两个元素的下标, 比如从数组{1,3,5,7,8}中找出和为8的两个元素的下标分别为(0,3)和(1,2)

func main() {
	a := [...]int{1, 3, 5, 7, 8}
	// 找出和为8的两个元素的下标为(0,3)和(1,2)
	// 定义两个for循环, 外层的从第一个开始遍历
	// 内层的for循环从外层后面的那个开始找
	// 它们两个数的和为8
	for i := 0; i < len(a); i++ {
		for j := i + 1; j < len(a); j++ {
			if a[i]+a[j] == 8 {
				fmt.Printf("(%d,%d)\n", i, j)
			}
		}
	}
}
